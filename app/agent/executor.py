# AGENT CREATION
# =====================================================

def create_agent_executor(
    user_id: Optional[int] = None,
    llm_provider: str = "openai",
    llm_model: str = None,
    verbose: bool = True
) -> AgentExecutor:
    """
    Create a configured LangChain agent executor.
    
    Args:
        user_id: User ID for context (optional)
        llm_provider: LLM provider to use
        llm_model: Specific model name
        verbose: Whether to print agent reasoning
    
    Returns:
        Configured AgentExecutor
    """
    
    # Get LLM
    llm = get_llm(provider=llm_provider, model=llm_model)
    
    # Create tools
    tools = create_tools()
    
    # Create prompt template
    prompt = ChatPromptTemplate.from_messages([
        ("system", SYSTEM_PROMPT),
        MessagesPlaceholder(variable_name="chat_history", optional=True),
        ("human", USER_PROMPT_TEMPLATE),
        MessagesPlaceholder(variable_name="agent_scratchpad"),
    ])
    
    # Create memory
    memory = ConversationBufferMemory(
        memory_key="chat_history",
        return_messages=True,
        output_key="output"
    )
    
    # Create agent
    agent = create_openai_tools_agent(llm, tools, prompt)
    
    # Create executor
    executor = AgentExecutor(
        agent=agent,
        tools=tools,
        memory=memory,
        verbose=verbose,
        max_iterations=15,  # Prevent infinite loops
        max_execution_time=300,  # 5 minute timeout
        handle_parsing_errors=True,
        return_intermediate_steps=True
    )
    
    return executor

# =====================================================
# CONVENIENCE FUNCTIONS
# =====================================================

async def run_agent(
    user_input: str,
    user_id: int,
    llm_provider: str = "openai",
    llm_model: str = None
) -> Dict[str, Any]:
    """
    Run the agent with user input.
    
    Args:
        user_input: User's request/query
        user_id: User ID for database operations
        llm_provider: LLM provider
        llm_model: Specific model name
    
    Returns:
        Agent response with output and intermediate steps
    """
    
    executor = create_agent_executor(
        user_id=user_id,
        llm_provider=llm_provider,
        llm_model=llm_model
    )
    
    result = await executor.ainvoke({
        "input": user_input,
        "user_id": user_id
    })
    
    return result

